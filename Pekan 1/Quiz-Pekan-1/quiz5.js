var jenisKelamin = "P";
var nama = "Sri";

// isilah variabel tersebut dengan "L" atau "P"
// isilah variabel nama

// jika "L" maka output yang dihasilkan adalah "bapak" + nama
// jika "P" maka output yang dihasilkan adalah "ibu" + nama

if (jenisKelamin == "L") {
  console.log("Bapak " + nama);
} else if (jenisKelamin == "P") {
  console.log("Ibu " + nama);
} else {
  console.log("unknown");
}
